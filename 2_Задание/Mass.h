#ifndef MASS_H // Защита от повторного включения
#define MASS_H 

#include <iostream>  // Для потоков ввода-вывода
#include <cstdlib>   // Для функций работы с памятью и случайными числами
#include <ctime>     // Для работы со временем

// Пространство имен для функций работы с массивами
namespace Mass  
{
    // Функция заполнения массива случайными числами
    // Заполняет массив случайными числами в заданном диапазоне
    // double* arr - указатель на массив для заполнения
    // int n - размер массива
    // double min - минимальное значение (по умолчанию -10.0)
    // double max - максимальное значение (по умолчанию 10.0)
    void fillRandom(double* arr, int n, double min = -10.0, double max = 10.0);

    // Функция вывода массива на экран
    // Выводит массив на экран по 10 элементов в строке
    // const double* arr - указатель на массив для вывода
    // n - размер массива
    void printArray(const double* arr, int n);

    // Функция вычисления суммы a1/0! + a2/1! + ... + an/(n-1)!
    // Вычисляет сумму a1/0! + a2/1! + ... + an/(n-1)!
    // const double* arr - указатель на массив
    // n - размер массива
    double SumFactorialDiv(const double* arr, int n);

    // Функция ручного ввода массива
    // Ввод массива с клавиатуры
    // double* arr - указатель на массив для заполнения
    // int n - размер массива
    void inputArray(double* arr, int n);

} // Конец namespace Mass

// Пространство имен для файловых операций
namespace fileSL  
{ 
    // Текстовые файлы
    
    // Функция сохранения массива в текстовый файл
    // Сохраняет массив в текстовый файл
    // const double* arr - указатель на массив для сохранения
    // int n - размер массива
    // const char* filename - имя файла для сохранения
    void saveFileText(const double* arr, int n, const char* filename);
    
    // Функция загрузки массива из текстового файла
    // Загружает массив из текстового файла
    // int& n - ссылка на переменную для размера массива (заполняется функцией)
    // const char* filename - имя файла для загрузки
    // Указатель на загруженный массив (требует delete[])
    double* loadFileText(int& n, const char* filename);

    // Бинарные файлы

    // Функция сохранения массива в бинарный файл
    // Сохраняет массив в бинарный файл
    // const double* arr - указатель на массив для сохранения
    // int n - размер массива
    // const char* filename - имя файла для сохранения   
    void saveFileBin(const double* arr, int n, const char* filename);
    
    // Функция загрузки массива из бинарного файла
    // Загружает массив из бинарного файла
    // int& n - ссылка на переменную для размера массива (заполняется функцией)
    // const char* filename - имя файла для загрузки
    // Указатель на загруженный массив (требует delete[])
    double* loadFileBin(int& n, const char* filename);

} // Конец namespace fileSL

#endif // MASS_H