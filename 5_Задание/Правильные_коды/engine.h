#ifndef engine_h
#define engine_h

#include <string>  // Подключение библиотеки для работы со строками

// Класс Engine описывает двигатель транспортного средства.
// Используется в композиции (двигатель — часть машины, не существует без неё).
class Engine {
private:
    int horsepower;       // Мощность двигателя в лошадиных силах
    std::string type;     // Тип двигателя

public:
    // Конструктор класса Engine
    // hp — мощность двигателя, t — тип двигателя
    // Если аргументы не переданы, используются значения по умолчанию
    Engine(int hp = 0, const std::string& t = "");

    // Геттер для мощности: возвращает текущую мощность двигателя
    int getHorsepower() const;

    // Сеттер для мощности: устанавливает новую мощность
    void setHorsepower(int value);

    // Геттер для типа: возвращает строку с типом двигателя
    std::string getType() const;

    // Сеттер для типа: устанавливает новый тип двигателя
    void setType(const std::string& value);
};

#endif // engine_h